package com.unsia.pbo.latihan3;

public class OperatorAritmatikaKonstan {
    public static void main(String[] args) {
        int a = 4; int b = 5; int c = 7; int d = 4;
        int e = 5; int f = 2; int g = 7; int h = 3;
        int i = 6; int j = 4;
        System.out.println("Perkalian : " + (a * b));
        System.out.println("Pembagian : " + (c / d));
        System.out.println("Sisa Pembagian : " + (e % f));
        System.out.println("Penjumlahan : " + (g + h));
        System.out.println("Pengurangan : " + (i - j));
    }
}